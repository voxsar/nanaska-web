// Helper to generate all non-empty subsets (power set) of an array
function generatePowerSet<T>(arr: T[]): T[][] {
	const result: T[][] = [];
	const n = arr.length;
	// Iterate through all possible binary combinations (2^n - 1, excluding empty set)
	for (let i = 1; i < (1 << n); i++) {
		const subset: T[] = [];
		for (let j = 0; j < n; j++) {
			if (i & (1 << j)) {
				subset.push(arr[j]);
			}
		}
		result.push(subset);
	}
	return result;
}

